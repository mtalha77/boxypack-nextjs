import React from 'react';
import { Metadata } from 'next';
import { navigationData } from '../../../../data/navigationData';
import ProductPageTemplate from '../../../../components/product-page/page';

export const metadata: Metadata = {
  title: 'Deluxe Candy Boxes | Premium Custom Packaging',
  description: 'Boxy Pack delivers premium custom candy boxes with fast turnaround, free shipping, and vibrant designs for standout product presentation. Shop Today.',
};

const CustomCandyBoxesPage = () => {
  const section = navigationData.find(s => s.slug === 'product-by-industry');
  const category = section?.categories?.find(c => c.slug === 'bakery-boxes');
  const subcategory = category?.subcategories.find(sc => sc.slug === 'custom-candy-boxes');
  
  if (!section || !category || !subcategory) {
    return <div>Subcategory not found</div>;
  }

  return (
    <ProductPageTemplate
      section={section}
      category={category}
      subcategory={subcategory}
      slug="custom-candy-boxes"
      pageType="subcategory"
    />
  );
};

export default CustomCandyBoxesPage;