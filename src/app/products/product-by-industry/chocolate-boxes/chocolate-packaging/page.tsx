import React from 'react';
import { Metadata } from 'next';
import { navigationData } from '../../../../data/navigationData';
import ProductPageTemplate from '../../../../components/product-page/page';

export const metadata: Metadata = {
  title: 'Chocolate Packaging | Premium Sweet Box Solutions',
  description: 'Boxy Pack produces high-quality chocolate packaging with fast turnaround, free shipping, and elegant design for professional and attractive sweet presentation. Customize Now.',
};

const ChocolatePackagingPage = () => {
  const section = navigationData.find(s => s.slug === 'product-by-industry');
  const category = section?.categories?.find(c => c.slug === 'chocolate-boxes');
  const subcategory = category?.subcategories.find(sc => sc.slug === 'chocolate-packaging');
  
  if (!section || !category || !subcategory) {
    return <div>Subcategory not found</div>;
  }

  return (
    <ProductPageTemplate
      section={section}
      category={category}
      subcategory={subcategory}
      slug="chocolate-packaging"
      pageType="subcategory"
    />
  );
};

export default ChocolatePackagingPage;