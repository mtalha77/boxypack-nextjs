import React from 'react';
import { Metadata } from 'next';
import { navigationData } from '../../../../data/navigationData';
import ProductPageTemplate from '../../../../components/product-page/page';

export const metadata: Metadata = {
  title: 'Round Pizza Boxes | Premium Custom Packaging',
  description: 'Boxy Pack produces premium round pizza boxes with fast turnaround, free shipping, and sturdy design for professional and fresh pizza presentation. Customize Now.',
};

const RoundPizzaBoxesPage = () => {
  const section = navigationData.find(s => s.slug === 'product-by-industry');
  const category = section?.categories?.find(c => c.slug === 'pizza-boxes');
  const subcategory = category?.subcategories.find(sc => sc.slug === 'round-pizza-boxes');
  
  if (!section || !category || !subcategory) {
    return <div>Subcategory not found</div>;
  }

  return (
    <ProductPageTemplate
      section={section}
      category={category}
      subcategory={subcategory}
      slug="round-pizza-boxes"
      pageType="subcategory"
    />
  );
};

export default RoundPizzaBoxesPage;