import React from 'react';
import { Metadata } from 'next';
import { navigationData } from '../../../../data/navigationData';
import ProductPageTemplate from '../../../../components/product-page/page';

export const metadata: Metadata = {
  title: 'Custom Cream Boxes | Premium Cosmetic Packaging',
  description: 'Boxy Pack delivers premium custom cream boxes with fast turnaround, free shipping, and professional design for standout skincare packaging. Order Now.',
};

const CustomCreamBoxesPage = () => {
  const section = navigationData.find(s => s.slug === 'product-by-industry');
  const category = section?.categories?.find(c => c.slug === 'cosmetic-boxes');
  const subcategory = category?.subcategories.find(sc => sc.slug === 'custom-cream-boxes');
  
  if (!section || !category || !subcategory) {
    return <div>Subcategory not found</div>;
  }

  return (
    <ProductPageTemplate
      section={section}
      category={category}
      subcategory={subcategory}
      slug="custom-cream-boxes"
      pageType="subcategory"
    />
  );
};

export default CustomCreamBoxesPage;