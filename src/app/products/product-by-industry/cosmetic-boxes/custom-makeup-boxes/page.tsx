import React from 'react';
import { Metadata } from 'next';
import { navigationData } from '../../../../data/navigationData';
import ProductPageTemplate from '../../../../components/product-page/page';

export const metadata: Metadata = {
  title: 'Custom Makeup Boxes | Stylish Premium Packaging',
  description: 'Boxy Pack produces high-quality custom makeup boxes with fast turnaround, free shipping, and premium designs to enhance beauty product appeal. Shop Today.',
};

const CustomMakeupBoxesPage = () => {
  const section = navigationData.find(s => s.slug === 'product-by-industry');
  const category = section?.categories?.find(c => c.slug === 'cosmetic-boxes');
  const subcategory = category?.subcategories.find(sc => sc.slug === 'custom-makeup-boxes');
  
  if (!section || !category || !subcategory) {
    return <div>Subcategory not found</div>;
  }

  return (
    <ProductPageTemplate
      section={section}
      category={category}
      subcategory={subcategory}
      slug="custom-makeup-boxes"
      pageType="subcategory"
    />
  );
};

export default CustomMakeupBoxesPage;